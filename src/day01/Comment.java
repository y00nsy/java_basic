// 자바는 클래스라는 블록에서 코드가 시작됩니다
public class Comment {

    /* 메인 함수는 프로그램의 시작 지점이 되며,
    없으면 프로그램을 실행할 수 없습니다. */
    public static void main(String[] args) {
        int a = 10;
        int b = 20;
        // 줄 맞출때 ctrl + alt + l
        // 빨간줄 뜰때 alt + enter => 도움말

        System.out.println(a + b);
        // 실행 단축키 ctrl + shift + f10
    }
}
